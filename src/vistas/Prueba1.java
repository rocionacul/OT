    private void ponerPaises() {
        comboPais.removeAllItems();
        for (Pais p : datos.getPaises()){
            comboPais.addItem(p.getNombre());
        }
        comboPais.setSelectedIndex(0);
        ponerProvincias();
        
    }

    private void ponerProvincias() {
        comboProvincia.removeAllItems();
        for (Provincia p : datos.getPaises().get(comboPais.getSelectedIndex()).getProvincias()){
            comboProvincia.addItem(p.getNombre());
        }
        comboProvincia.setSelectedIndex(0);
        ponerCiudades();
    }

    private void ponerCiudades() {
       comboCiudad.removeAllItems();
       for (Ciudad c : datos.getPaises().get(comboPais.getSelectedIndex()).getProvincias().get(comboProvincia.getSelectedIndex()).getCiudades()){
           comboCiudad.addItem(c.getNombre());
       }
    }
