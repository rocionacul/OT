    public void ponerPaquetes(ArrayList<Paquete> paquetes) {
      comboPaquete.removeAllItems();
      for(Paquete p : paquetes){
          comboPaquete.addItem(p.getNombre());
      }
    }

    public void mostrarSalidas(ArrayList<Salida> salidas) {
       DefaultTableModel matriz = new DefaultTableModel();
       matriz.addColumn("Cupo");
       matriz.addColumn("Fecha Inicio");
       matriz.addColumn("Fecha Regreso");
       matriz.addColumn("Numero de Salida");
       matriz.addColumn("Estado");
    for(Salida s : salidas){
    //if(s.getEstadoSalida()==EstadoSalida.EnPausa){
        matriz.addRow(new Object[]{
        s.getCupo(),
        s.getFechaInicio(),
        s.getFechaFin(),
        s.getNumero(),
        s.getEstadoSalida()
    });
    //}
    }
    tablaSalidasdePaquete.setModel(matriz);
}

    public void ponerBases(ArrayList<Base> bases) {
       comboBases.removeAllItems();
       for(Base b : bases){
           comboBases.addItem(b.getTipo());
       }
    }

    public void ponerBasesTabla(Salida s) {
       DefaultTableModel matriz = new DefaultTableModel();
       matriz.addColumn("Base");
       matriz.addColumn("Precio");
       for(Tarifa t : s.getTarifas()){
           matriz.addRow(new Object[]{
               t.getBase().getTipo(),
               t.getPrecio()
           });
       }
       tablaTarifas.setModel(matriz);
    }
