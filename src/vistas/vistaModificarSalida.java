    public void ponerPaquetes(ArrayList<Paquete> paquetes) {
       comboPaquete.removeAllItems();
      for(Paquete p : paquetes){
          comboPaquete.addItem(p.getNombre());
      }
    }

    public void ponerSalidas(ArrayList<Salida> salidas) {
       comboSalidas.removeAllItems();
       for(Salida s : salidas){
                   if(s.getEstadoSalida()==EstadoSalida.EnPausa){
           comboSalidas.addItem(s);
       }
       }     
    }

    public void ponerBasesTabla(Salida s) {
       DefaultTableModel matriz = new DefaultTableModel();
       matriz.addColumn("Base");
       matriz.addColumn("Precio");
       for(Tarifa t : s.getTarifas()){
           matriz.addRow(new Object[]{
               t.getBase().getTipo(),
               t.getPrecio()
           });
       }
       tablaTarifas.setModel(matriz);
    }
 
